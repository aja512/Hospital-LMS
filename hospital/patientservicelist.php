  <li><big><strong>Adult Immunisation</strong></big> <br><p>
The term vaccination or immunisation brings to mind teary-eyed toddlers in a paediatrician's clinic. However, contrary to tradition belief, vaccines don't stop at childhood! By vaccinating yourself, you reduce your chance of contracting serious illnesses and passing them along. <br>
Immunisation is the process whereby a person is made immune or resistant to an infectious disease, typically by the administration of a vaccine. It teaches the body's immune system to recognize and defend against harmful bacteria and viruses before getting an infection.
</p>
   <ul>
     <li><strong>Need for adult immunisation – It isn't just for children</strong><br> 
        <ul> 
          <li>
            <p style="font-weight: bold;">Adults with chronic conditions:</p>
            <p> 
              Individuals with asthma, heart, liver, lung, kidney disease, diabetes, smokers and immunosuppressed individuals (post-transplant, HIV positive, steroid treatment, etc.) are at increased risk of serious diseases like pneumonia and meningitis caused by Streptococcus pneumoniae.<br>
              Even caretakers may require vaccinations to prevent transmission of infections such as chicken pox.
            </p>
          </li>
          <li>
            <p style="font-weight: bold;">Universal vaccination:</p> 
            <p> The body's immune response from vaccination declines over time. Thus, an annual vaccine is required for optimal protection. Secondly, the formulation of the flu vaccine is reviewed each year and sometimes updated, which makes it necessary to take it again. </p>
          </li>
          <li>
            <p style="font-weight: bold;">Immunisation for travel:</p>
            <p> It's important to have the right vaccinations before you travel to some countries. For example:As a high risk location, vaccinations are essential for those visiting Bali and Indonesia.Meningococcal vaccine is necessary if you are travelling to Saudi Arabia to take part in the Hajj. Students travelling abroad for higher studies are required to get certain vaccinations too. </p>
          </li>
          <li>
            <p style="font-weight: bold;">Incomplete childhood vaccination:</p>
            <p>Inadequate levels of immunization against childhood diseases are a significant problem.  If you have not been vaccinated for infections like measles or chicken pox as a child and not had the disease itself, you stand a greater chance of acquiring the infection in a more severe form.  </p>
          </li>
          <li>
            <p style="font-weight: bold;">Periodic booster shots:</p> 
            <p>The immunity derived from childhood vaccinations like Tetanus Toxoid and Pertussis vaccine may wane over time. To maintain immunity, you may require booster shots. </p>
          </li>
          <li>
            <p style="font-weight: bold;">Preventing transmission to babies who are too young for vaccines:</p>
            <p> Influenza vaccine is transmitted from the mother to the new-born as it is not recommended for infants who are less than 6 months.</p>
          </li>
          <li>
            <p style="font-weight: bold;">Newer vaccines have been developed:</p>
            <p> Some vaccinations recommended for adults are relatively new such as the HPV vaccine. It protects sexually active adults from human papilloma virus that can lead to cervical as well as genital cancers. </p>
          </li>
          <li>
            <p style="font-weight: bold;">Some vaccines are especially for adults:</p>
            <p> Herpes zoster is a virus that may live on in your nervous tissues for years even after chickenpox is treated. It can cause a severe and painful skin rash. In addition to that, its risk increases with age. The Herpes Zoster vaccine is recommended for those 60 years and above. </p>
          </li>
          <li>
            <p style="font-weight: bold;">Post exposure prophylaxis:</p> 
            <p> Non-immune adults may be protected by timely vaccinations after being exposed to HIV to prevent HIV infection. </p>
          </li>
         </ul>

     </li>
